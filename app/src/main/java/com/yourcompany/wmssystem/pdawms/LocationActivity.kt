    private fun loadNetworkImage(imageUrl: String, imageView: ImageView, fallbackImage: Int) {
        // ‰ΩøÁî®ÂçèÁ®ãÂú®ÂêéÂè∞Á∫øÁ®ãÂä†ËΩΩÂõæÁâá
        CoroutineScope(Dispatchers.IO).launch {
